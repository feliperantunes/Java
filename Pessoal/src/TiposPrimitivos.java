public class TiposPrimitivos {
    static void main(String[] args) {

        int idade = 32; // 2147483647 é o max para tipo int, armazena 32 bits
        Long numerosGrandes = 99999L; //Precisa sempre ter um L no final, armazena 64 bits
        double altura = 1.80; // armazena 16 bits
        char letra = 'A'; // armazena 16 bits

        System.out.println(idade);
        System.out.println(numerosGrandes);
        System.out.println(altura);
        System.out.println(letra);

    }
}

