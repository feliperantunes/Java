public class operadoresAritimeticos {
    static void main(String[] args) {
        int a = 4;
        a += 2;
        System.out.printf("Somar e atribuir: %d \n", a);

        int b = 2;
        b-= 2;
        System.out.printf("Subitrair e atribuir: %d \n",b);

        int c = 7;
        c*= 2;
        System.out.printf("Multiplicar e atribuir: %d \n",c);

        int d = 1;
        d/= 1;
        System.out.printf("Dividir e atriubir: %d \n",d);

        int e = 10;
        e%= 3;
        System.out.printf("Resto e atribuir: %d \n", e);
    }
}
